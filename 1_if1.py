def main():

    age = int(input('Input age: '))

    if age < 2:
        print('small')
    elif 2 <= age <= 6:
        print('kindergarten')
    elif 6 < age <= 18:
        print('school')
    elif 18 < age < 23:
        print('university')
    else:
        print('JOB!!!')


if __name__ == "__main__":
    main()
